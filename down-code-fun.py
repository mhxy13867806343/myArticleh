import re,requests,os
pattern = r'https?://[^\s<>"]+\.png'
png_urls = re.findall(pattern, html)

# 循环遍历所有匹配到的URL
for img_url in png_urls:
    # 从URL中提取图片名称
    img_name = img_url.split('name=')[-1]

    # 检查企业文件夹是否存在，不存在则创建
    if not os.path.exists('./enterprise'):
        os.makedirs('./enterprise')

    # 完整的URL
    full_url = img_url

    # 下载图片
    response = requests.get(full_url)
    if response.status_code == 200:
        # 构建保存路径
        save_path = os.path.join('./enterprise', img_name)

        # 保存图片到本地
        with open(save_path, 'wb') as f:
            f.write(response.content)